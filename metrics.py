def convert_to_checkboxes_corrected(filters):
    checkboxes_data = {}
    for category, items in filters.items():
        # Correcting the category key transformation
        category_key = category.replace(
            "_filter", ""
        ).lower()  # e.g. 'Sectors_filter' -> 'sectors'
        checkboxes = []
        for i, (label, checked) in enumerate(items.items(), start=1):
            checkboxes.append({"id": i, "label": label, "checked": checked})
        checkboxes_data[category_key] = checkboxes
    return checkboxes_data


