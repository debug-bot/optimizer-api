def convert_to_constraints_data(buffers):
    constraints_data = {"names": [], "units": []}

    # Iterate over the buffer data and transform it
    for buffer_name, buffer_values in buffers.items():
        # Extract the category name from the buffer name (e.g., "buffer_Sectors" -> "Sectors")
        category_name = buffer_name.split("_")[1]
        constraints_data["names"].append(category_name)

        units_list = []
        for i, (name, value) in enumerate(buffer_values.items(), start=1):
            unit = {"id": i, "name": name, "value": [value, value, value]}
            units_list.append(unit)

        constraints_data["units"].append(units_list)

    return constraints_data


